class Solution {
    public int lastStoneWeightII(int[] stones) {
        // ä¸ Q494 é—®é¢˜ç±»ä¼¼ï¼Œæ±‚è§£é—®é¢˜ä» å¯èƒ½æ€§ æ•°é‡ â†’ å®¹é‡æœ€å¤§çš„å¯èƒ½æ€§
        int sum = Arrays.stream(stones).sum();
        // target = sum - neg * 2

        // dp[i] æŒ‡æ˜¯å¦å¯ä»¥æ»¡è¶³å®¹é‡ä¸º i çš„é€‰æ‹©
        int maxNeg = sum / 2;
        boolean[] dp = new boolean[maxNeg + 1];
        dp[0] = true;
        for (int i = 0; i < stones.length; i++) {
            for (int j = maxNeg; j >= stones[i]; j--) {
                dp[j] = dp[j] || dp[j - stones[i]];
            }
        }
        for (int j = maxNeg; j >= 0; j--) {
            if (dp[j]) return sum - j * 2;
        }
        return sum;
    }
}
