    public boolean isValidSudoku(char[][] board) {
        // row[i][j] è¡¨ç¤ºç¬¬ i è¡Œï¼Œjæ•°å­—å‡ºç°æ¬¡æ•°
        // col[i][j] è¡¨ç¤ºç¬¬ i åˆ—ï¼Œjæ•°å­—å‡ºç°æ¬¡æ•°
        // box[i][j][k] è¡¨ç¤ºå·¦ä¸Šè§’ä¸ºi,j çš„ä¹å®«æ ¼ä¸­ï¼Œjæ•°å­—å‡ºç°æ¬¡æ•°

        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                if (board[i][j] != '.') {
                    int c = board[i][j] - '0' - 1;
                    if (++row[i][c] > 1) {
                        return false;
                    }
                    if (++col[j][c] > 1) {
                        return false;
                    }
                    if (++box[i / 3][j / 3][c] > 1) {
                        return false;
                    }
                }
            }
        }
        return true;
    }

    int[][] row = new int[9][9];
    int[][] col = new int[9][9];
    int[][][] box = new int[3][3][9];

    private boolean backtrack(char[][] board, int r, int c) {
        if (c == 9) {
            return backtrack(board, r + 1, 0);
        }
        if (r == 9) return true;
        for (int i = r; i < 9; i++) {
            for (int j = c; j < 9; j++) {
                if (board[i][j] != '.') {
                    return backtrack(board, i, j + 1);
                }

                for (char ch = '1'; ch <= '9'; ch++) {
                    int idx = ch - '1';
                    if (row[i][idx] >= 1 || col[j][idx] >= 1 || box[i / 3][j / 3][idx] >= 1) {
                        continue;
                    }

                    row[i][idx]++;
                    col[j][idx]++;
                    box[i / 3][j / 3][idx]++;
                    board[i][j] = ch;
                    if (backtrack(board, i, j + 1)) return true;
                    board[i][j] = '.';
                    row[i][idx]--;
                    col[j][idx]--;
                    box[i / 3][j / 3][idx]--;
                }
                return false;
            }
        }
        return false;
    }

    public void solveSudoku(char[][] board) {
        // å·²ç»å¡«å¥½çš„
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                if (board[i][j] != '.') {
                    int c = board[i][j] - '0' - 1;
                    ++row[i][c];
                    ++col[j][c];
                    ++box[i / 3][j / 3][c];
                }
            }
        }
        backtrack(board, 0, 0);
    }
